CREATE TABLE CUSTOMER
(CustomerID NUMBER(6),
CustomerName VARCHAR2(20),
City VARCHAR2(20),
State CHAR(2),
PostalCode CHAR(5),
PhoneNumber CHAR(10),
Birthday DATE,
RegistrationDate DATE,
    CONSTRAINT customer_customerid_pk PRIMARY KEY(CustomerID));

CREATE TABLE ORDER_RECORD
(OrderID NUMBER(10),
OrderDate DATE,
PaymentMethod CHAR(2),
OrderType CHAR(1),
CustomerID NUMBER(6),
    CONSTRAINT order_record_orderid_pk PRIMARY KEY(OrderId),
    CONSTRAINT order_record_paymentmethod_ck
        CHECK (PaymentMethod in ('CS', 'CC', 'PP')),
    CONSTRAINT order_record_ordertype_ck
        CHECK (OrderType in ('P', 'D')),
    CONSTRAINT order_record_customerid_fk FOREIGN KEY (CustomerID)
        REFERENCES CUSTOMER(CustomerID));
        
CREATE TABLE DIGITAL_ORDER
(D_OrderID NUMBER(10),
SubscriptionOption CHAR(1),
    CONSTRAINT digital_order_d_orderid_pk PRIMARY KEY(D_OrderID),
    CONSTRAINT digital_order_d_orderid_fk FOREIGN KEY (D_OrderID)
        REFERENCES ORDER_RECORD(OrderID),
    CONSTRAINT digital_order_suboption_ck
        CHECK (SubscriptionOption in ('M', 'Y')));
        
CREATE TABLE PHYSICAL_ORDER
(P_OrderID NUMBER(10),
DeliveryOption CHAR(1),
    CONSTRAINT physical_order_p_orderid_pk PRIMARY KEY(P_OrderID),
    CONSTRAINT physical_order_p_orderid_fk FOREIGN KEY (P_OrderID)
        REFERENCES ORDER_RECORD(OrderID),
    CONSTRAINT physical_order_delivoption_ck
        CHECK (DeliveryOption in ('F', 'S', 'E')));

CREATE TABLE SONG
(SongID NUMBER(10),
SongTitle VARCHAR2(50),
PlayTime NUMBER(3),
    CONSTRAINT song_songid_pk PRIMARY KEY(SongID));
  
CREATE TABLE ALBUM
(AlbumID NUMBER(10),
AlbumTitle VARCHAR2(50),
AlbumPrice NUMBER(3),
ReleaseDate DATE,
    CONSTRAINT album_albumid_pk PRIMARY KEY(AlbumID));
   
CREATE TABLE PLAY_HISTORY
(D_OrderID NUMBER(10),
SongID NUMBER(10),
PlayDate DATE,
PlayCount NUMBER(3),
    CONSTRAINT play_history_orderidsongid_pk PRIMARY KEY(D_OrderID, SongID),
    CONSTRAINT play_history_d_orderid_fk FOREIGN KEY (D_OrderID)
        REFERENCES DIGITAL_ORDER(D_OrderID),
    CONSTRAINT play_hisotry_songid_fk FOREIGN KEY (SongID)
        REFERENCES SONG(SongID));
        
CREATE TABLE ORDER_LINE
(P_OrderID NUMBER(10),
AlbumID NUMBER(10),
QuantitiesOrdered NUMBER(3),
    CONSTRAINT order_line_orderidalbumid_pk PRIMARY KEY (P_OrderID, AlbumID),
    CONSTRAINT order_line_p_orderid_fk FOREIGN KEY (P_OrderID)
        REFERENCES PHYSICAL_ORDER(P_OrderID),
    CONSTRAINT order_line_albumid_fk FOREIGN KEY (AlbumID)
        REFERENCES ALBUM (AlbumID));

CREATE TABLE ARTIST
(ArtistID NUMBER(10),
ArtistName VARCHAR2(20),
DebutDate DATE,
    CONSTRAINT artist_artistid_pk PRIMARY KEY (ArtistID));
    
CREATE TABLE GENRE
(GenreID NUMBER(4),
GenreName VARCHAR2(50),
    CONSTRAINT genre_genreid_pk PRIMARY KEY (GenreID));
 
CREATE TABLE CONTAINS
 (SongID NUMBER(10),
 AlbumID NUMBER(10),
    CONSTRAINT contains_songidalbumid_pk PRIMARY KEY (SongID, AlbumID),
    CONSTRAINT contains_songid_fk FOREIGN KEY (SongID)
        REFERENCES SONG(SongID),
    CONSTRAINT contains_albumid_fk FOREIGN KEY (AlbumID)
        REFERENCES ALBUM(AlbumID));

CREATE TABLE WRITES
(ArtistID NUMBER(10),
SongID NUMBER(10),
    CONSTRAINT writes_artistidsongid_pk PRIMARY KEY (ArtistID, SongID),
    CONSTRAINT writes_artistid_fk FOREIGN KEY (ArtistID)
        REFERENCES ARTIST(ArtistID),
    CONSTRAINT writes_songid_fk FOREIGN KEY (SongID)
        REFERENCES SONG(SongID));

CREATE TABLE BELONGSTO
(SongID NUMBER(10),
GenreID NUMBER(4),
    CONSTRAINT belongsto_songidgenreid_pk PRIMARY KEY (SongID, GenreID),
    CONSTRAINT belongsto_songid_fk FOREIGN KEY (SongID)
        REFERENCES SONG(SongID),
    CONSTRAINT belongsto_genreid_fk FOREIGN KEY (GenreID)
        REFERENCES GENRE(GenreID));

